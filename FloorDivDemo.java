public class FloorDivDemo {
	public static void main(String[] args) {
		System.out.println("7 / 3 = " + (7 / 3));

		System.out.println("7 % 3 = " + (7 % 3));

		System.out.println("-7 / 3 = " + (-7 / 3));
		System.out.println("-7 % 3 = " + (-7 % 3));

		System.out.println("Math.floorDiv(-7, 3) = " + Math.floorDiv(-7, 3));
		System.out.println("Math.floorMod(-7, 3) = " + Math.floorMod(-7, 3));
	}
}